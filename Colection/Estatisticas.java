package Colection;

public class Estatisticas {

    private int Ataque = 0;
    private int Defesa = 0;
    private int AtaqueEspecial = 0;
    private int DefesaEspecial = 0;
    private int PontosDeVida = 0;
    private int Velocidade = 0;

    public Estatisticas(int ataque, int defesa, int ataqueEspecial, int defesaEspecial, int pontosDeVida,
            int velocidade) {

        Ataque = ataque;
        Defesa = defesa;
        AtaqueEspecial = ataqueEspecial;
        DefesaEspecial = defesaEspecial;
        PontosDeVida = pontosDeVida;
        Velocidade = velocidade;
        
    }

}
